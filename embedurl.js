const url = url => {
	return promise = new Promise((resolve, reject) => {
		if (validateUrl(url)) {
			manageMedia.embedUrl(url)
			.then(media => resolve(media))
			.catch(error => reject(error))
		} else
			reject({"error": "invalid url"});
	})	
};

exports.url = url;